import { POKEMON } from '../pokemon/pokemon.js';

function getPokemonFromLocalStorage(key) {
    const localPokemon = window.localStorage.getItem(key);

    return JSON.parse(localPokemon);
}

const pokemonResultsArray = getPokemonFromLocalStorage(POKEMON);

// create new array
const captureData = pokemonResultsArray
    .filter((item) => item.encounter)
    .map((item) => ({
        name: item.pokemon,
        captures: item.capture,
        encounters: item.encounter
    }));

const pokemonLabels = captureData.map((item) => item.name);
const pokemonEncounters = captureData.map((item) => item.encounters);
const pokemonCaptures = captureData.map((item) => item.captures);

